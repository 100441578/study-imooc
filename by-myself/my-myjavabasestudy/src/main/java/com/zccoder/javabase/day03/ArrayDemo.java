package com.zccoder.javabase.day03;

public class ArrayDemo 
{
    public static void main(String[] args) 
    {
        //元素类型[]数组名=new元素类型[元素个数或数组长度]；

        //需求：想定义一个可以存储3个整数的容器。
        int[] x=new int[3];
        x[0]=59;


        //打印数组中角标为0的元素的值。
        System.out.println(x[0]);
    }
}
